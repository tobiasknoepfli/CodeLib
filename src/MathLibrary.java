public class MathLibrary {
    public static void main(String[] args) {

//        ABSOLUTE VALUE
        System.out.println(Math.abs(-50));          //Returns the absolute value

//        ARC COSINE
        System.out.println(Math.acos(1));           //Returns the arc cosine of a value

//        ARC SINE
        System.out.println(Math.asin(1));           //Returns the arc sine of a value

//        ARC TANGENT
        System.out.println(Math.atan(1));           //Returns the arc tangent of a value

//        CUBE ROOT
        System.out.println(Math.cbrt(27));          //Returns the cube root of a double value

//        COSINE
        System.out.println(Math.cos(Math.PI));      //Returns the trigonometric cosine of an angle

//        E^X
        System.out.println(Math.exp(2));            //Returns Euler 's number e raised to the power of a double value

//        E^-1
        System.out.println(Math.expm1(2));          //Returns ex -1

//        LOGARITHM
        System.out.println(Math.log(10));           //Returns the natural logarithm of a value

//        LOG 10
        System.out.println(Math.log10(10));         //Returns the base 10 logarithm of a value

//        MAXIMUM
        System.out.println(Math.max(5,10));         //Returns the greater of two values

//        MINIMUM
        System.out.println(Math.min(5,10));         //Returns the smaller of two values.

//        EXPONENTIATION
        System.out.println(Math.pow(5,2));          //Returns the value of the first argument raised to the power of the second argument

//        RANDOM
        System.out.println(Math.random());          //Returns a double value with a positive sign, greater than or equal to 0.0 and less than 1.0

//        ROUND INTEGER
        System.out.println(Math.rint(5.27));        //Returns the double value that is closest in value to the argument and is equal to a mathematical integer

//        ROUND
        System.out.println(Math.round(5.225));      //Returns the closest int to the argument, with ties rounding to positive infinity

//        SINE
        System.out.println(Math.sin(Math.PI));      //Returns the trigonometric sine of an angle

//        SQUARE ROOT
        System.out.println(Math.sqrt(81));          //Returns the correctly rounded positive square root of a double value

//        TANGENT
        System.out.println(Math.tan(Math.PI));      //Returns the trigonometric tangent of an angle

//        RADIAN TO DEGREE
        System.out.println(Math.toDegrees(1.45));   //Converts an angle measured in radians to an approximately equivalent angle measured in degrees

//        DEGREE TO RADIAN
        System.out.println(Math.toRadians(90));     //Converts an angle measured in radians to an approximately equivalent angle measured in degrees.
    }
}
